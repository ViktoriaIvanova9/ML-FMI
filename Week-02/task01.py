# Какво е класификация? - Предсказването на "характеристиките" на множество от данни(наблюдение). т.е. слагане на етикет
# Какво е двоична класификация? - Когато за всеки елемент от наблюдението трябва да се даде двоичен отговор - да/не, има/няма
# Коя може да бъде търсената променлива за двоична класификация? - churn
# Кой термин се използва за да опише останалите колони? - feature/характеристика